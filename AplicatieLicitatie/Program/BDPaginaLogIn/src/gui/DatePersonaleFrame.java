    public boolean verifData(){
        if(nume.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Introduceti un nume");
            return false;
        }
        else if(prenume.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Introduceti un prenume");
            return false;
        }
        else if(datanastere.getDateFormatString().length() == 0){
            JOptionPane.showMessageDialog(null, "Introduceti data nastere");
            return false;
        }
        else if(email.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Introduceti email");
           return false;
        }    
        else 
            return true;
    }
