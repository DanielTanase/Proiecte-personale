    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        //modificarea datelor in cazul in care cerintele campurilor sunt
        //indeplinite
        if(verifData() && verifData2()){
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String stringDate = dateFormat.format(datanastere.getDate());
        final java.sql.Date sqlDate = java.sql.Date.valueOf(stringDate);
        conn = MySqlConnect.ConnectDB();
        String Sql = "Update utilizator Set Nume = ?, Prenume = ?, `Adresa Email` = ?, `Data Nastere` = ?, Sex = ? Where `Utilizator ID` = ?;";
        try{
            pst = conn.prepareStatement(Sql);
            pst.setString(1,nume.getText());
            pst.setString(2,prenume.getText());
            pst.setString(3,email.getText());
            pst.setDate(4,sqlDate);
            pst.setString(5,sex.getSelectedItem().toString());
            pst.setString(6, LoginFrame.getInfo().getString("Utilizator ID"));
            
            int i = pst.executeUpdate();
            if(i != 0){
                JOptionPane.showMessageDialog(null,"Actualizare reusita");
            }
            else{
                JOptionPane.showMessageDialog(null, "Actualizare nereusita", "Access Denied", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    }//GEN-LAST:event_jButton1MouseClicked

